'use client';

import React, { useState } from 'react';
import { useRouter } from 'next/navigation';
import { useAuth } from '@/modules/auth/context/AuthContext';
import { Lock, LogIn, AlertCircle } from 'lucide-react';

export default function LoginPage() {
    const [loginInput, setLoginInput] = useState('');
    const [error, setError] = useState('');
    const [isSubmitting, setIsSubmitting] = useState(false);
    const { login } = useAuth();
    const router = useRouter();

    const handleSubmit = async (e: React.FormEvent) => {
        e.preventDefault();
        setError('');
        setIsSubmitting(true);

        await new Promise(resolve => setTimeout(resolve, 500));

        const success = login(loginInput);

        if (success) {
            router.push('/dashboard/structure');
        } else {
            setError('Login invalide. Veuillez vérifier vos identifiants.');
        }

        setIsSubmitting(false);
    };

    const containerStyle: React.CSSProperties = {
        minHeight: '100vh',
        display: 'flex',
        alignItems: 'center',
        justifyContent: 'center',
        position: 'relative',
        overflow: 'hidden'
    };

    const videoStyle: React.CSSProperties = {
        position: 'absolute',
        top: 0,
        left: 0,
        width: '100%',
        height: '100%',
        objectFit: 'cover'
    };

    const cardStyle: React.CSSProperties = {
        position: 'relative',
        width: '90%',
        maxWidth: '420px',
        padding: '40px',
        backgroundColor: '#ffffff',
        borderRadius: '24px',
        boxShadow: '0 25px 80px rgba(0,0,0,0.25)'
    };

    return (
        <div style={containerStyle}>
            <video
                autoPlay
                muted
                loop
                playsInline
                style={videoStyle}
            >
                <source src="/tt.mp4" type="video/mp4" />
            </video>

            <div style={cardStyle}>
                <div style={{ textAlign: 'center', marginBottom: '32px' }}>
                    <div style={{
                        width: '72px',
                        height: '72px',
                        borderRadius: '16px',
                        background: 'linear-gradient(135deg, #5B8DEF 0%, #4169B8 100%)',
                        display: 'flex',
                        alignItems: 'center',
                        justifyContent: 'center',
                        margin: '0 auto 16px',
                        boxShadow: '0 8px 24px rgba(91,141,239,0.4)'
                    }}>
                        <Lock size={32} color="white" strokeWidth={2} />
                    </div>
                    <h1 style={{
                        fontSize: '24px',
                        fontWeight: '700',
                        color: '#1a202c',
                        marginBottom: '8px'
                    }}>
                        École ISM
                    </h1>
                    <p style={{
                        fontSize: '14px',
                        color: '#64748b'
                    }}>
                        Connectez-vous pour accéder au tableau de bord
                    </p>
                </div>

                {error && (
                    <div style={{
                        display: 'flex',
                        alignItems: 'center',
                        gap: '8px',
                        padding: '12px 16px',
                        background: '#FEF2F2',
                        border: '1px solid #FECACA',
                        borderRadius: '10px',
                        marginBottom: '20px',
                        color: '#DC2626',
                        fontSize: '14px'
                    }}>
                        <AlertCircle size={18} />
                        {error}
                    </div>
                )}

                <form onSubmit={handleSubmit}>
                    <div style={{ marginBottom: '24px' }}>
                        <label style={{
                            display: 'block',
                            fontSize: '14px',
                            fontWeight: '600',
                            color: '#4a5568',
                            marginBottom: '8px'
                        }}>
                            Login
                        </label>
                        <input
                            type="text"
                            value={loginInput}
                            onChange={(e) => setLoginInput(e.target.value)}
                            placeholder="Entrez votre login"
                            required
                            style={{
                                width: '100%',
                                padding: '14px 16px',
                                borderRadius: '12px',
                                border: '1.5px solid #e2e8f0',
                                fontSize: '15px',
                                outline: 'none',
                                transition: 'all 0.2s ease',
                                fontFamily: 'inherit',
                                background: '#f8fafc'
                            }}
                            onFocus={(e: any) => {
                                e.target.style.borderColor = '#5B8DEF';
                                e.target.style.background = 'white';
                            }}
                            onBlur={(e: any) => {
                                e.target.style.borderColor = '#e2e8f0';
                                e.target.style.background = '#f8fafc';
                            }}
                        />
                    </div>

                    <button
                        type="submit"
                        disabled={isSubmitting}
                        style={{
                            width: '100%',
                            padding: '14px 24px',
                            borderRadius: '12px',
                            border: 'none',
                            background: isSubmitting
                                ? '#94a3b8'
                                : 'linear-gradient(135deg, #5B8DEF 0%, #4169B8 100%)',
                            color: 'white',
                            fontSize: '15px',
                            fontWeight: '600',
                            cursor: isSubmitting ? 'not-allowed' : 'pointer',
                            transition: 'all 0.2s ease',
                            boxShadow: isSubmitting
                                ? 'none'
                                : '0 8px 20px rgba(91,141,239,0.35)',
                            display: 'flex',
                            alignItems: 'center',
                            justifyContent: 'center',
                            gap: '8px'
                        }}
                    >
                        {isSubmitting ? (
                            <>
                                <span style={{
                                    width: '18px',
                                    height: '18px',
                                    border: '2px solid white',
                                    borderTopColor: 'transparent',
                                    borderRadius: '50%',
                                    animation: 'spin 0.8s linear infinite'
                                }} />
                                Connexion...
                            </>
                        ) : (
                            <>
                                <LogIn size={18} />
                                Se connecter
                            </>
                        )}
                    </button>
                </form>

                <div style={{
                    marginTop: '28px',
                    padding: '16px',
                    background: '#f8fafc',
                    borderRadius: '10px',
                    textAlign: 'center'
                }}>
                    <p style={{
                        fontSize: '12px',
                        color: '#64748b',
                        marginBottom: '8px'
                    }}>
                        Logins disponibles pour test:
                    </p>
                    <div style={{
                        display: 'flex',
                        flexWrap: 'wrap',
                        gap: '6px',
                        justifyContent: 'center'
                    }}>
                        {['Diarra123p', 'Astouacp', 'Lucien23op', 'Mar998M', 'Kande783O'].map(cred => (
                            <span key={cred} style={{
                                padding: '4px 8px',
                                background: 'white',
                                border: '1px solid #e2e8f0',
                                borderRadius: '6px',
                                fontSize: '11px',
                                color: '#475569',
                                fontFamily: 'monospace'
                            }}>
                                {cred}
                            </span>
                        ))}
                    </div>
                </div>
            </div>
        </div>
    );
}

